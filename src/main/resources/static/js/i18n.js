window.switchLanguage = function (lang) {
    localStorage.setItem('appLang', lang);
    translatePage(lang);
};

function translatePage(lang) {
    document.documentElement.lang = lang;

    // Fast text translation
    walkTextNodes(document.body, lang);
    walkPlaceholders(document.body, lang);
}

function walkTextNodes(node, lang) {
    node.childNodes.forEach(child => {
        if (child.nodeType === 3) { // TEXT_NODE
            if (child.parentElement && child.parentElement.tagName === 'SCRIPT' || child.parentElement.tagName === 'STYLE') return;

            if (child._originalText === undefined) {
                child._originalText = child.nodeValue;
            }

            let original = child._originalText.trim();
            if (original === "") return;

            // Simple exact match
            if (translations[original]) {
                child.nodeValue = child._originalText.replace(original, translations[original][lang]);
            } else {
                // Try case insensitive or partial match for large blocks (basic attempt)
                let matchedKey = Object.keys(translations).find(k => k.toLowerCase() === original.toLowerCase());
                if (matchedKey) {
                    child.nodeValue = child._originalText.replace(original, translations[matchedKey][lang]);
                }
            }
        } else if (child.nodeType === 1) { // ELEMENT_NODE
            walkTextNodes(child, lang);
        }
    });
}

function walkPlaceholders(node, lang) {
    const inputs = node.querySelectorAll('input[placeholder], textarea[placeholder]');
    inputs.forEach(input => {
        if (input._originalPlaceholder === undefined) {
            input._originalPlaceholder = input.placeholder;
        }
        let original = input._originalPlaceholder.trim();
        if (translations[original]) {
            input.placeholder = translations[original][lang];
        }
    });
}
